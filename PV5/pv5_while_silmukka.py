# while-silmukassa laskurimuuttuja pitää luoda itse
counter = 1

# niin kauan kuin counter on alle tai tasan 10, ajetaan while-silmukan sisällä oleva koodi
while counter <= 10:
    print(counter)

    counter = counter + 1  # counter pitää muista aina kasvatta yhdellä, ilman sitä jää ikuiseen silmukkaan pyörimään